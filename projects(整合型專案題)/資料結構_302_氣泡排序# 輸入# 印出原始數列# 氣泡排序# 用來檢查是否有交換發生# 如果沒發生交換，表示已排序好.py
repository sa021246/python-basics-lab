# 輸入
n = int(input())
nums = list(map(int, input().split()))

# 印出原始數列
print(' '.join(map(str, nums)))

# 氣泡排序
for i in range(n - 1):
    swapped = False  # 用來檢查是否有交換發生
    for j in range(n - 1 - i):
        if nums[j] > nums[j + 1]:
            nums[j], nums[j + 1] = nums[j + 1], nums[j]
            swapped = True
    if swapped:
        print(' '.join(map(str, nums)))
    else:
        break  # 如果沒發生交換，表示已排序好