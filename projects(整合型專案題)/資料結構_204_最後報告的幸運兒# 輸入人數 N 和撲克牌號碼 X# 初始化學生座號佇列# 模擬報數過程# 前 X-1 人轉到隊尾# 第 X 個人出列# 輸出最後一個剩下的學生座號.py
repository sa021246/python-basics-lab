#=================================================================================

from collections import deque

# 輸入人數 N 和撲克牌號碼 X
n = int(input())
x = int(input())

# 初始化學生座號佇列
students = deque(range(1, n + 1))

# 模擬報數過程
while len(students) > 1:
    for _ in range(x - 1):
        students.append(students.popleft())  # 前 X-1 人轉到隊尾
    students.popleft()  # 第 X 個人出列

# 輸出最後一個剩下的學生座號
print(students[0])
