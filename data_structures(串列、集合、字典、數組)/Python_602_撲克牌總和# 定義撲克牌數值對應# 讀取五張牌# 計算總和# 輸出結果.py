# 定義撲克牌數值對應
card_values = {
    'A': 1, 'J': 11, 'Q': 12, 'K': 13
}

# 讀取五張牌
cards = [input().strip() for _ in range(5)]

# 計算總和
total = sum(int(card_values.get(card, card)) for card in cards)

# 輸出結果
print(total)