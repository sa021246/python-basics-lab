#===================================================================================

from collections import Counter

# 讀取 10 個整數
nums = [int(input()) for _ in range(10)]

# 計算數字出現的頻率
counter = Counter(nums)

# 找到眾數（出現最多次的數字）
mode = max(counter, key=counter.get)

# 輸出眾數及其出現的次數
print(mode)
print(counter[mode])
