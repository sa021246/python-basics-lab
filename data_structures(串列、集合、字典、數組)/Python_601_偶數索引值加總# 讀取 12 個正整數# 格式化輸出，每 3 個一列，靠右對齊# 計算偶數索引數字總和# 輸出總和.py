# 讀取 12 個正整數
nums = [int(input()) for _ in range(12)]

# 格式化輸出，每 3 個一列，靠右對齊
for i in range(0, 12, 3):
    print(f"{nums[i]:3} {nums[i+1]:3} {nums[i+2]:3}")

# 計算偶數索引數字總和
even_index_sum = sum(nums[i] for i in range(0, 12, 2))

# 輸出總和
print(even_index_sum)
