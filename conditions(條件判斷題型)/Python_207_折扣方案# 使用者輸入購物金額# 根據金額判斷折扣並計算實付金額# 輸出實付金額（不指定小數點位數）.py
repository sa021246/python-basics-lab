# 使用者輸入購物金額
amount = float(input())

# 根據金額判斷折扣並計算實付金額
if amount >= 38000:
    total = amount * 0.7
elif amount >= 28000:
    total = amount * 0.8
elif amount >= 18000:
    total = amount * 0.9
else:  # amount >= 8000
    total = amount * 0.95

# 輸出實付金額（不指定小數點位數）
print(total)

