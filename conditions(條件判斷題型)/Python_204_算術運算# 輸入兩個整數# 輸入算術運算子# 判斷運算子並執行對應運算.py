# 輸入兩個整數
a = int(input())
b = int(input())

# 輸入算術運算子
op = input()

# 判斷運算子並執行對應運算
if op == '+':
    print(a + b)
elif op == '-':
    print(a - b)
elif op == '*':
    print(a * b)
elif op == '/':
    print(a / b)
elif op == '//':
    print(a // b)
elif op == '%':
    print(a % b)
else:
    print("Invalid operator")

