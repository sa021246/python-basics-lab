# 請使用者輸入一個正整數 n，限制 n 必須小於 10
n = int(input("請輸入一個正整數 n (n < 10): "))

# 確保輸入的 n 符合範圍要求
if n < 1 or n >= 10:
    print("輸入錯誤，請輸入 1~9 的整數。")
else:
    # 使用巢狀迴圈產生 n*n 的乘法表
    for i in range(1, n + 1):  # 外層迴圈控制列
        for j in range(1, n + 1):  # 內層迴圈控制行
            # 格式化每一個運算式，左對齊，數字部分欄寬為 4
            print(f"{j:2} * {i:2} = {i * j:<4}", end="")  # 不換行
        print()  # 每列輸出完成後換行
