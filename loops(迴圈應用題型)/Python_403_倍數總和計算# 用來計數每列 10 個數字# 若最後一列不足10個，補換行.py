#===============================================================================

a = int(input())
b = int(input())

count = 0
total = 0
line = 0  # 用來計數每列 10 個數字

for i in range(a, b + 1):
    if i % 4 == 0 or i % 9 == 0:
        print(f"{i:<4}", end='')
        count += 1
        total += i
        line += 1
        if line == 10:
            print()
            line = 0

if line != 0:
    print()  # 若最後一列不足10個，補換行

print(count)
print(total)
