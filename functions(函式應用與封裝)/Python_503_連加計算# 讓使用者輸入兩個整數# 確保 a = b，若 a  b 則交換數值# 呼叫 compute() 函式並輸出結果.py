#============================================================================

def compute(a, b):
    return sum(range(a, b + 1))

# 讓使用者輸入兩個整數
try:
    a = int(input().strip())
    b = int(input().strip())

    # 確保 a <= b，若 a > b 則交換數值
    if a > b:
        a, b = b, a

    # 呼叫 compute() 函式並輸出結果
    print(compute(a, b))
except ValueError:
    print("error")

