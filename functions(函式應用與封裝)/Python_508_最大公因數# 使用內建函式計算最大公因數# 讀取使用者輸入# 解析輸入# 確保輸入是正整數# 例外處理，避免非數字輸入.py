#============================================================================

import math

def compute(x, y):
    return math.gcd(x, y)  # 使用內建函式計算最大公因數

# 讀取使用者輸入
try:
    x, y = map(int, input().strip().split(','))  # 解析輸入
    if x > 0 and y > 0:
        print(compute(x, y))
    else:
        print("Invalid input.")  # 確保輸入是正整數
except ValueError:
    print("Invalid input.")  # 例外處理，避免非數字輸入