#============================================================================

import math

def compute(p, q):
    return math.gcd(p, q)  # 計算最大公因數

# 讀取使用者輸入
try:
    x, y = map(int, input().strip().split(','))  # 解析第一個分數
    m, n = map(int, input().strip().split(','))  # 解析第二個分數
    
    if x > 0 and y > 0 and m > 0 and n > 0:  # 確保輸入為正整數
        # 計算通分後的分子與分母
        p = x * n + m * y
        q = y * n
        
        # 簡化分數
        gcd = compute(p, q)
        p //= gcd
        q //= gcd

        # 輸出結果
        print(f"{x}/{y} + {m}/{n} = {p}/{q}")
    else:
        print("Invalid input.")  # 確保數字皆為正整數
except ValueError:
    print("Invalid input.")  # 例外處理，避免非數字輸入
