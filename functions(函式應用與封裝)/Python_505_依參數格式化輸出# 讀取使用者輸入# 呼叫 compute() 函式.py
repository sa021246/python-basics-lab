#============================================================================

def compute(a, x, y):
    for _ in range(y):
        print((a + " ") * x)

# 讀取使用者輸入
a = input().strip()
try:
    x = int(input().strip())
    y = int(input().strip())

    # 呼叫 compute() 函式
    compute(a, x, y)
except ValueError:
    print("error")
