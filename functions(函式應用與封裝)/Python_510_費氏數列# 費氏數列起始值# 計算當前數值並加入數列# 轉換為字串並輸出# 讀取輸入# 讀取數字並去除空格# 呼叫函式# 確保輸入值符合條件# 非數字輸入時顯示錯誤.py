def compute(num):
    fib = [0, 1]  # 費氏數列起始值
    for _ in range(2, num):  
        fib.append(fib[-1] + fib[-2])  # 計算當前數值並加入數列
    print(" ".join(map(str, fib)))  # 轉換為字串並輸出

# 讀取輸入
try:
    num = int(input().strip())  # 讀取數字並去除空格
    if num >= 2:  
        compute(num)  # 呼叫函式
    else:
        print("Invalid input.")  # 確保輸入值符合條件
except ValueError:
    print("Invalid input.")  # 非數字輸入時顯示錯誤
